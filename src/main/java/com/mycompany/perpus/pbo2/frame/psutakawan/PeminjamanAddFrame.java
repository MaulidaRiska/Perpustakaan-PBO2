    private void tbBukuKMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbBukuKMouseReleased
        TableModel model = tbBukuK.getModel();
        int barisTerpilih = tbBukuK.getSelectedRow();

        if(barisTerpilih >= 0){

            String idBuku = model.getValueAt(barisTerpilih,0).toString();
            String namaBuku = model.getValueAt(barisTerpilih,1).toString();
            String hargaBuku = model.getValueAt(barisTerpilih,3).toString();

            jtfNamaBuku.setText(namaBuku);
            jtfHargaBuku.setText(hargaBuku);

        }
    }//GEN-LAST:event_tbBukuKMouseReleased

