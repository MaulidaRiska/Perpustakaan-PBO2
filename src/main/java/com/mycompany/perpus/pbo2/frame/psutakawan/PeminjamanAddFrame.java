    public void tampilDataBuku(ArrayList<Buku> list){
        DefaultTableModel model = (DefaultTableModel) tbBukuK.getModel();
        model.setRowCount(0);
        
        if(list != null){   
            Object[] row = new Object[4];
            
            for (int i = 0; i < list.size(); i++) {
                row[0] = list.get(i).getId();
                row[1] = list.get(i).getNamaBuku();
                row[2] = list.get(i).getJenisBuku().getNamaJenisBuku();
                row[3] = list.get(i).getHarga();
                
                model.addRow(row);
            }
            
        }
    }
    
    
